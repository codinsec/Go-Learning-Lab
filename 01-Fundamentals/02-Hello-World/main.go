package main

import "fmt"

// main is the entry point of a Go program
// When you run the program, Go automatically calls the main function
func main() {
	// fmt.Println prints a line to the standard output
	fmt.Println("Hello, World!")
	fmt.Println("Welcome to Go Learning Lab!")

	// You can print multiple values
	fmt.Println("Go version:", "1.21+")
	fmt.Println("Learning path:", "Fundamentals")
}

